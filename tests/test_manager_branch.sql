\echo '=========================================='
\echo 'ТЕСТ 2: Региональное ограничение'
\echo '=========================================='

-- Менеджер Мария (филиал 1) должна видеть аренды филиала 1
SELECT '=== Менеджер Мария (филиал 1) ===' AS test_point;
SELECT 'Должна видеть аренды client_id=1,2 (Иван и Анна)' AS expected;

-- Фактические данные филиала 1
SELECT rental_id, client_id, branch_id, total_price, status 
FROM rentals 
WHERE branch_id = 1;

-- Менеджер Дмитрий (филиал 2) должен видеть аренды филиала 2
SELECT '=== Менеджер Дмитрий (филиал 2) ===' AS test_point;
SELECT 'Должен видеть аренды client_id=3 (Петр)' AS expected;

SELECT rental_id, client_id, branch_id, total_price, status 
FROM rentals 
WHERE branch_id = 2;

-- Проверяем политику для менеджеров
SELECT '=== Проверка политик RLS для менеджеров ===' AS test_point;
SELECT tablename, policyname, cmd, qual 
FROM pg_policies 
WHERE policyname LIKE '%manager%';

