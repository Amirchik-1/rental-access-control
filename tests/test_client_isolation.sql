SELECT '=== Данные Ивана ===' AS test_point;
SELECT rental_id, client_id, equipment_id, total_price, status 
FROM rentals 
WHERE client_id = (SELECT client_id FROM clients WHERE full_name LIKE '%Иванов%');

\echo '1.2. Попытка Ивана увидеть аренду Петра'
\echo 'Ожидается: 0 строк (если RLS работает)'
\echo '------------------------------------------'

-- Проверяем, что политика RLS активна
SELECT '=== Проверка политик RLS ===' AS test_point;
SELECT tablename, policyname, cmd, qual 
FROM pg_policies 
WHERE tablename = 'rentals' AND policyname LIKE '%client%';

