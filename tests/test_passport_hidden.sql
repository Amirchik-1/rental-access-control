\echo '=========================================='
\echo 'ТЕСТ 3: Защита паспортных данных'
\echo '=========================================='

-- Проверяем, что менеджер не видит паспортные данные
SELECT '=== Менеджер Мария (должна НЕ видеть passport_data) ===' AS test_point;

-- Проверяем права на столбец
SELECT '=== Права доступа к столбцу passport_data ===' AS test_point;
SELECT 
    grantee,
    table_name,
    column_name,
    privilege_type
FROM information_schema.column_privileges 
WHERE table_name = 'clients' 
    AND column_name = 'passport_data'
    AND grantee IN ('r_client', 'r_manager', 'r_admin');

-- Проверяем, что администратор ВИДИТ паспортные данные
SELECT '=== Администратор (должен видеть все паспорта) ===' AS test_point;
SELECT full_name, passport_data FROM clients;

-- Проверяем, что клиент видит ТОЛЬКО свои паспортные данные
SELECT '=== Клиент Иван (должен видеть только свой паспорт) ===' AS test_point;

